    private void jbtBuscarPorPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtBuscarPorPedidoActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
        DateFormat f = DateFormat.getDateInstance();
        Date d1 = null;

        String data = sdf.format(jcbDataPedido.getDate());

        try {
            d1 = sdf.parse(data);
        } catch (ParseException ex) {
            Logger.getLogger(PedidoRelatorio.class.getName()).log(Level.SEVERE, null, ex);
        }

        List<PedidoCompletoo> lista = new ArrayList<>();
        lista = buscarPedido(Integer.parseInt(jtfCodigoCliente.getText()), data);

        DefaultTableModel dtm = (DefaultTableModel) jtablePedido.getModel();
        dtm.setNumRows(0);
        if (lista.size() == 0) {
            JOptionPane.showMessageDialog(null, "Numero de pedidos √© : " + lista.size());
        } else {
            for (int i = 0; i < lista.size(); i++) {

                dtm.addRow(new Object[]{
                    lista.get(i).getId_pedido(),
                    lista.get(i).getNome_cliente(),
                    lista.get(i).getCpf_cliente(),
                    formatador.format(lista.get(i).getData()),
                    lista.get(i).getVALOR_TOTAL_PEDIDO(),
                    lista.get(i).getNome()

                });

                CardLayout card = (CardLayout) root.getLayout();
                card.show(root, "geracao");
                jlbResults.setText(lista.size() + "");
            }

    }//GEN-LAST:event_jbtBuscarPorPedidoActionPerformed
    }
