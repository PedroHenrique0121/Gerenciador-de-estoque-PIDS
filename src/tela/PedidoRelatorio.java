    private void jtablePedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtablePedidoMouseClicked
        int linha = jtablePedido.getSelectedRow();
        Long idPedido = Long.parseLong(jtablePedido.getValueAt(linha,0).toString());
        HashMap params = new HashMap<>();
            params.put("idPedido",idPedido );

            JasperPrint jp = null;
        try {
            jp = JasperFillManager.fillReport("src/relatorio/r/projeto.jasper", params, Conexao.conecta());
        } catch (SQLException ex) {
            Logger.getLogger(PedidoRelatorio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PedidoRelatorio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JRException ex) {
            Logger.getLogger(PedidoRelatorio.class.getName()).log(Level.SEVERE, null, ex);
        }
            JasperViewer jv = new JasperViewer(jp, false);

            jv.setVisible(true);
    }//GEN-LAST:event_jtablePedidoMouseClicked

